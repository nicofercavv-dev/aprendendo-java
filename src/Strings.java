public class Strings {

    public static void main(String[] args) {
        String x = " Eu sou uma puta de uma";
        System.out.println(x.length());
        System.out.println(x + " gostosa");
        System.out.println((x.contains("sou")));
        System.out.println(x.lastIndexOf("uma"));
        System.out.println(x.toUpperCase());
        System.out.println(x.trim());
        System.out.println(x.substring(8));
        System.out.println(x.equals(" Eu sou uma puta de uma"));
    }
}
